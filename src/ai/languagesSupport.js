import { AppToaster } from "../components/Toaster";

export function getValidLanguageCode(input) {
  if (!input) return "";
  let lggCode = input.toLowerCase().trim().slice(0, 2);
  if (supportedLanguage.includes(lggCode)) {
    AppToaster.clear();
    return lggCode;
  } else {
    AppToaster.show({
      message:
        "Live AI: Incorrect language code for transcription, see instructions in settings panel.",
    });
    return "";
  }
}

